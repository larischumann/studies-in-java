
public class TestaPontoFlutuante {
	public static void main (String[] args ) {
		double salario = 1250.70;
		System.out.println("Meu salário é " + salario + ".");
		
		double idade = 37;
		System.out.println("Minha idade é " + idade + ".");
		
		double divisao = 3.14 / 2;
		System.out.println("3.14 dividido por 2 é igual á " + divisao + ".");
		
		int outraDivisao = 5 / 2;
		System.out.println("5 dividido por 2 é igual á " + outraDivisao + " (int).");
		
		double novaTentativa = 5.0 / 2;
		System.out.println("5 dividido por 2 é igual á " + novaTentativa + " (double).");
	}
}
